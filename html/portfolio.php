	<h2>Production Notes</h2>
      
	<br />
	<br />
	<h2>Dead Space</h2> Work Performed: Score and Sound Design 
<br />
Here is an example of a current game trailer.  The mood is rather dark as it opens as a dream sequence.  There is a large amount of sci-fi and spacey effects in the intro, followed by an operating room environment and finally a few zombie types.  I used water droplets for a lot of the small object chatter that occurs before the rock tune and near the end of the sequence.  Also of note, I use velcro to create the trail of cracking that connects the zombies.
Most of the music was done in Logic, and the effects and mix were done in Pro Tools. 
	<br />
	<br />
<h2>Aflac</h2> Work Performed: Score, Sound Design and Voice Over
<br />
This piece illustrates a specific genre in sound design and scoring that I love and wanted to explore.  
I tried to pay homage to the original scoring philosophy on those old Warner Brothers cartoons. I used traditional orchestral sounds from Logic 8 throughout (except for the electric guitar at the very end) with the harmonic and rhythmic motion exaggerating the hit points.  The sounds that I chose for impacts were also in remembrance of those funny over-the-top sonic nuggets found in those cartoons, as they were often the punchline for the joke.  I ended up pitching up most of the voice over to give it that �cartoony� flavor; we had a lot of fun trying to conjure up a performance from the actors.  
	<br />
	<br />
	<h2>BreachAndClear</h2> Work Performed: Score & Sound Design 
<br />
This is a game trailer for <a href="http://www.fearthegun.com/">Gun Studios</a>.  I did the score in Logic using Kontakt instruments and the sound design and mix in Pro Tools.  
	<br />
	<br />
<h2>Sam</h2> Work Performed: Sound Design, Music Editing 
<br />
This piece showcases both my sound design skills and music editing chops. 
I did all the sound design in this project and used an existing score from a famous composer to illustrate my abilities of making existing music fit the film.  The soundtrack gives the flick a dark tone, which I think compliments the story nicely.  I did all the work in protools except for the score edit which was done in Audition. 
	<br />
	<br />
<h2>Facing Worlds</h2> Work Performed:  replacement of all ambiences, weapon pickups and discharges
<br />
This piece illustrates my ability to integrate sound design using the Unreal Editor.
I took an existing level and replaced the static sounds as well as recreated weapons pickups and discharges.  I added more ambient and static sounds that were previously there, such as the serpent sounds made from stretching rubber, handling a gel pack and krinkling bubble wrap.  The weapons pickups and discharges were mostly composites using real weapons fire with other unrelated sounds. 
	<br />
	<br />
<h2>Efile</h2> Work Performed: All sound elements including narration and incidental VO
<br />
This is a funny little commercial about a man, his tax return and a mailbox.  I exaggerated the interaction between the man and the mailbox with the impact sounds to add to the humor.  This project was done exclusively in Pro Tools.
<br />
<br />
<h2>Painting Day</h2> Work Performed: All sound design elements and music composition
<br />
Here�s another project that was partially done in Logic (soundtrack) and in Pro tools (sound design and final mix).  I wanted to recreate a slapstick type audioscape that emphasized a fairly large portion of the movement in this piece.  So, there is a lot of synchronization between the action, score and sound effects.  I used an acoustic guitar as well as virtual  guitar samples combined to create the main instrument for the score. 
<br />
<br />
<h2>Fancy Nancy</h2> Work Performed: All sound design, score and voice over
<br />
This piece is a unique twist on the audio book format with the use of a child narrator and a video representation of the actual book. 
I used my daughter as the narrator and I comped the performance together using Pro tools.  The sound design and music was then added within the same session. Each page of the book was scanned, sent to iPhoto for editing, and finally sent to iMovie to be united with the audio. 
<br />
<br />
<h2>Epsilon 3</h2> Work performed: joint composition, score mix
<br />
On this project I contributed an original score to go along with a full length film created by filmmaker Steve Rudolf.  Using a trio of guitar, bass and drums, all with lots of effects, we tracked most of this live, playing along with the edited film.  The scenes that we contributed to were mostly open ended exploration type sequences, so it freed us up quite a bit.  All work was performed in Pro tools on this one.  
<br />
<br />
<h2>Century 21</h2> Work Performed:  All sound design and dialog
<br />
On this one I replaced the original dialogue with different actors and created all the sound design using Pro Tools.  
<br />
<br />
<h2>Stevinson</h2> Work Performed: All sound design and dialog, music editing
<br />
This was a funny commercial about a man and his best friend who happened to be a cardboard representation of his local car dealer.  I chose a comical swing tune from a library to be soundtrack, replaced all the dialog and added all the ambience and effects.  
